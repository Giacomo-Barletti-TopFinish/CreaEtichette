CREATE TABLE ETI_ARTICOLI
(
   IDARTICOLI NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
  IDMAGAZZ	VARCHAR2(10 BYTE) NOT NULL,
  CODICEMODELLO	VARCHAR2(15 BYTE)	NOT NULL,	
  CODICECOLORE	VARCHAR2(15 BYTE)	NOT NULL,	
  COLORE VARCHAR2(30) NOT NULL,
  SKU VARCHAR2(12) NOT NULL,
  MODELLO VARCHAR2(50) NOT NULL,
  PARZIALE VARCHAR2(20) NULL,
  RTT VARCHAR2(20) NULL,
    CONSTRAINT PK_IDARTICOLI PRIMARY KEY (IDARTICOLI)
);

CREATE INDEX IDX_ETI_ARTICOLI_1 ON ETI_ARTICOLI(IDMAGAZZ);

